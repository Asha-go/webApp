<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <strong><span style="font-size: 16px">An introduction to Chinese alcohol</span></strong>
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    There is a common misconception in the West that Chinese people do not and can not drink alcohol. In reality, alcohol and drinking culture plays a very important role in Chinese society and varies vastly across different areas. For example, in Shandong province, the most popular alcohol types are Bai Jiu (白酒) and beer (啤酒). It is believed that the best way to show hospitality and ensure that a guest feels happy and welcome is to get them drunk. Shandongers believe that alcohol can help improve relationships between people and that everyone is at their most honest and open when drunk. Moreover, in Shandong there are many complicated drinking rules (seating arrangement, roles, toasts, etc.) that have been inherited from ancient times. This drinking custom makes you feel like you must drink as much as possible in order to be a good and polite guest. Many areas in Northern China are like this and most Westerners will be hard pressed to out-drink a Northerner! On the other hand, in Zhejiang province, the most popular alcohol is yellow rice wine (黄酒), few people drink alcohol and there isn’t such a deeply rooted drinking culture. Similarly, in the South less people drink alcohol because it is considered bad for health.
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    This varied drinking culture has given rise to many different kinds of alcohol in China. In this article, let’s explore the most popular alcoholic beverages in China and which ones expats may like!
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <strong><span style="text-decoration:underline;">1. Beer (啤酒)</span></strong>
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    Beer was introduced into China in the early 20th century and has gradually become one of the main alcoholic beverages consumed by Chinese people. The most common type in China is lager and it’s usually lighter (around 4%) than most European beers. The most popular local beer brands are Tsingtao (青岛啤酒), Xuehua (雪花), Harbin (哈尔滨), and Yanjing (燕京).
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <img width="217" height="217" src="https://ashago.oss-cn-zhangjiakou.aliyuncs.com/Asha%20Go%20China%20website%202020/Food%26Drinks/The%20most%20famous%20Chinese%20tea/1.jpg?OSSAccessKeyId=LTAI4FcWHUa9TfvGA9oMY3fE&Expires=1001596975455&Signature=f7vsbAZnnmVII6ujW9Dwcw%2B7P8c%3D" alt="图片包含 瓶子, 冰箱, 照片, 食物 描述已自动生成"/>
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    Tsingtao beer is one of the most famous and earliest established breweries in China. It was founded in 1903 in Qingdao city as the Germania-Brauerei Tsingtao Co., Ltd. by German and British merchants. Tsingtao Brewery now owns more than 60 local breweries across 20 provinces in China. It is the leading domestic beer brand in terms of both company size and market share. Tsingtao also produces wheat, IPA, and other types of beer. The quality is quite good considering its price.
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <img width="325" height="217" src="https://ashago.oss-cn-zhangjiakou.aliyuncs.com/Asha%20Go%20China%20website%202020/Food%26Drinks/The%20most%20famous%20Chinese%20tea/2.jpg?OSSAccessKeyId=LTAI4FcWHUa9TfvGA9oMY3fE&Expires=1001596975496&Signature=0jj%2BdE1%2FzhBPpMn2YMPvuyolZIo%3D" alt="杯子里有饮料 描述已自动生成"/>
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    It is worth mentioning that many Chinese people like to drink warm beer. So, don’t forget to tell the waiters you want cold beer (冰的) when you order. This is because Chinese people generally believe that cold beverages are bad for the stomach. If you are not satisfied with industrial beers, craft beers are becoming more and more popular in China.
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <strong><span style="text-decoration:underline;">2. Bai Jiu (白酒)</span></strong>
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    Bai Jiu is a traditional Chinese spirit which is very popular (especially in Northern China). It’s very strong! Its alcoholic percentage varies between 30%—65%. The most famous and expensive Bai Jiu is Moutai（茅台） and it is considered the national alcohol of China. For one bottle of Moutai you can expect to pay upwards of RMB2,000 depending on its age and whether it’s a special edition.
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <img width="228" height="228" src="https://ashago.oss-cn-zhangjiakou.aliyuncs.com/Asha%20Go%20China%20website%202020/Food%26Drinks/The%20most%20famous%20Chinese%20tea/3.jpg?OSSAccessKeyId=LTAI4FcWHUa9TfvGA9oMY3fE&Expires=1001596975531&Signature=m0WpKBi22fXrW5SFxgp0hURIK2U%3D" alt="图片包含 游戏机, 食物 描述已自动生成"/>
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    Apart from Moutai, the popular Bai Jiu brands are Er Guo Tou (二锅头), Wu Liang Ye (五粮液) and Fen Jiu（汾酒）. Bai Jiu has a very unique and distinguished taste. Although not that popular drink amoung most expatriates, it’s definitely worth a try!
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <img width="256" height="150" src="https://ashago.oss-cn-zhangjiakou.aliyuncs.com/Asha%20Go%20China%20website%202020/Food%26Drinks/The%20most%20famous%20Chinese%20tea/4.jpg?OSSAccessKeyId=LTAI4FcWHUa9TfvGA9oMY3fE&Expires=1001596975560&Signature=MbfkazyO1tINpJjrzRKPPb7Nsls%3D" alt="手里拿着杯子 描述已自动生成"/>
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <strong><span style="text-decoration:underline;">3. Yellow Rice Wine (黄酒)</span></strong>
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    Yellow rice wine is one of the oldest alcoholic beverages in the world - it has been around for more than three thousand years! It is popular in Shanghai, Zhejiang, and Jiangsu area. Its taste is very mild and the alcohol content is usually between 14%-20%.
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <img width="256" height="171" src="https://ashago.oss-cn-zhangjiakou.aliyuncs.com/Asha%20Go%20China%20website%202020/Food%26Drinks/The%20most%20famous%20Chinese%20tea/5.jpeg?OSSAccessKeyId=LTAI4FcWHUa9TfvGA9oMY3fE&Expires=1001596975589&Signature=OLQYPdyYyYWKEdvwmLmZvB2jLcQ%3D" alt="桌子上有杯子和碗 描述已自动生成"/>
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    When it comes to yellow rice wine - the older the better! Chinese people believe drinking a moderate amount of yellow rice wine is good for health. It is also used&nbsp; to cook some seafood and meat dishes. It can be drank both cold and hot.
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    The most popular brands are Kuai Ji Shan (会稽山) and Gu Yue Long Shan (古越龙山). Both brands are from Shaoxing city, Zhejiang province.
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <img width="185" height="185" src="https://ashago.oss-cn-zhangjiakou.aliyuncs.com/Asha%20Go%20China%20website%202020/Food%26Drinks/The%20most%20famous%20Chinese%20tea/6.jpg?OSSAccessKeyId=LTAI4FcWHUa9TfvGA9oMY3fE&Expires=1001596975620&Signature=FmhF8qDZHiCfqM8ooAkykX4Pjis%3D" alt="图片包含 瓶子, 室内, 红酒, 食物 描述已自动生成"/>
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <strong><span style="text-decoration:underline;">4. Wine</span></strong>
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    You are probably already aware that China is not famous for its wine. However, in recent years wine has become more popular and China is starting to develop its wine making industry. There are many vineyards and wine sellers emerging with this new trend. The most famous of these being Changyu and GreatWall... but the quality and taste of these wines is somewhat questionable...
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    The best Chinese vineyards are mainly located in Ningxia and Xinjiang. The climate in these areas is suitable for grape growth with plenty of sunshine, dry air and large temperature difference between day and night. If you would like to try local wine, we recommend Helanshan Donglu 贺兰山东麓 from Ningxia and Puchang 蒲昌 (<a href="http://puchangwine.com/" style="color: rgb(149, 79, 114)">http://puchangwine.com/</a>) from Xinjiang.
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <img width="162" height="220" src="https://ashago.oss-cn-zhangjiakou.aliyuncs.com/Asha%20Go%20China%20website%202020/Food%26Drinks/The%20most%20famous%20Chinese%20tea/7.jpg?OSSAccessKeyId=LTAI4FcWHUa9TfvGA9oMY3fE&Expires=1001596975727&Signature=3UfuTOUyNpiITo%2Bp83p%2F4rgBT8g%3D" alt="图片包含 桌子, 食物, 瓶子, 香蕉 描述已自动生成"/>
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    <strong><span style="text-decoration:underline;">5. Local alcoholic beverages</span></strong>
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    There are many local homemade alcoholic beverages in different areas, that are not so popular nationalwide but certainly worth trying:
</p>
<p style=";text-align: justify;font-size: 14px;font-family: DengXian;white-space: normal">
    &nbsp;
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
    Sweet rice wine (米酒) ——Yunnan, Zhejiang, Hubei, etc.
</p>
<p style=";text-align: center;font-size: 14px;font-family: DengXian;white-space: normal">
